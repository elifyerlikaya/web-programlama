function yesno() {

    var checkboxvar = document.getElementById("tıkla");
    var labelvar = document.getElementById("text");
    if (!checkboxvar.checked) {
        labelvar.innerHTML = "";
    }
    else {
        labelvar.innerHTML = "Fethiye";
    }
}
